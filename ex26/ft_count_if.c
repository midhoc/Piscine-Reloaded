int		ft_count_if(char **tab, int (*f)(char*))
{
	if (tab[0] == '\0')
		return (0);
	if (f(tab[0]))
		return (1 + ft_count_if(++tab, f));
	return (0 + ft_count_if(++tab, f));
}
